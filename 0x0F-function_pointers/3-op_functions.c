/**
 * op_add - Computes sum of two integers
 * @a: First integer
 * @b: Second integer
 * Return: Sum of intergers a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Compute difference of two integers
 * @a: First integer
 * @b: Second integer
 * Return: Difference a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul- Computes the multiplication of a and b
 * @a: First integer
 * @b: Second integer
 * Return: Product a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Computes division of two integers
 * @a: First integer
 * @b: Second integer
 * Return: Result of the division of a by b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Computes the remainder
 * @a: Numerator
 * @b: Denominator
 * Return: Remainder of the division of a by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
